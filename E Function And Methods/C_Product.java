// Product of a and b


public class C_Product {

    public static int multiply(int a,int b){
        int product = a * b;
        return product;
    }
    public static void main(String args[]){
        int prod;

        prod = multiply(3, 5);
        System.out.println("Product is : "+prod);
    }
} 

// Output:-

// Product is : 15