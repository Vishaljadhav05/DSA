//Print hello world 5 times using
public class G_PrintUsingDoWhile
{
    public static void main(String[] args) {
        
        int counter = 1;

        do{
            System.out.println("Hello World");
            counter++;
        }while(counter <= 5);
    }
}

//Output:-

/*
Hello World
Hello World
Hello World
Hello World
Hello World
*/
