public class A_LargestOfTwo 
{
    public static void main(String[] args) 
    {
        int a = 20;
        int b = 7;

        if (a>=b) {
            System.out.println("a is largest of 2");
        }
        else{
            System.out.println("b is largest of 2");
        }
    }
}

//Output:-

//a is largest of 2